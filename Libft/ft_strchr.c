char	*strchr(const char *s, int c)
{
	char	*p;
	int		i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
		{
			*p = c;
			return (p);
		}
		i++;
	}
	return (0);
}
